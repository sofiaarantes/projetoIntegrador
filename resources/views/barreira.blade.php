                                    <a data-bs-toggle="collapse" data-bs-target="#accordion-list-2" class="collapsed"><span>02</span> Como age? <i class="bx bx-chevron-down icon-show"></i><i class="bx bx-chevron-up icon-close"></i></a>
                                    <div id="accordion-list-2" class="collapse" data-bs-parent=".accordion-list">
                                        <p>
                                            Ele é introduzido no interior da vagina, de modo a recobrir todo o colo do útero. Recomenda-se o seu uso com espermicida, a fim de aumentar sua eficácia. É importante destacar que, diferentemente da camisinha, esse método só deve ser adquirido após consulta
                                            médica, pois será feita a medicação da mulher para que o diafragma se encaixe perfeitamente.
                                        </p>
                                    </div>
                                </li>
                                <li>
                                    <a data-bs-toggle="collapse" data-bs-target="#accordion-list-3" class="collapsed"><span>03</span> Quais são seus benefícios? <i class="bx bx-chevron-down icon-show"></i><i class="bx bx-chevron-up icon-close"></i></a>
                                    <div id="accordion-list-3" class="collapse" data-bs-parent=".accordion-list">
                                        <p>
                                            Não tem efeitos colaterais hormonais; O uso pode ser interrompido a qualquer momento; É fácil de ser usado; Raramente é sentido pelo parceiro; Pode ter uma duração de até 2 anos; Não consegue entrar no útero ou se perder no corpo da mulher.
                                        </p>
                                    </div>
                                </li>
                                <li>
                                    <a data-bs-toggle="collapse" data-bs-target="#accordion-list-4" class="collapsed"><span>04</span> Como usar? <i class="bx bx-chevron-down icon-show"></i><i class="bx bx-chevron-up icon-close"></i></a>
                                    <div id="accordion-list-4" class="collapse" data-bs-parent=".accordion-list">
                                        <p>
                                            O diafragma é bastante simples de colocar e deve ser colocado cerca de 15 a 30 minutos antes da relação sexual, sendo recomendado sempre utilizar um gel espermicida, seguindo os seguintes passos: <br> <br>Colocar
                                            o gel espermicida no diafragma e dobrá-lo com a parte redonda para baixo; Introduzir o diafragma na vagina com a parte redonda para baixo; Empurrar o diafragma e ajustá-lo para ficar corretamente colocado. Em
                                            alguns casos, a mulher pode colocar um pouco de lubrificante para facilitar a colocação do diafragma. Após a relação sexual, esse contraceptivo deve ser retirado após cerca de 6 a 8 horas, pois é o tempo médio
                                            de sobrevivência do espermatozoide. No entanto, é importante não deixar por mais tempo, pois caso contrário pode haver o favorecimento de infecções. <br><br>Depois de retirado, o diafragma deve ser lavado com
                                            água fria e sabão neutro, secado naturalmente e guardado na sua embalagem, podendo ser reutilizado por cerca de no máximo 2 anos. No entanto, caso seja verificado um furo ou uma rachadura, esteja ficando enrugado
                                            ou caso a mulher engravide ou ganhe peso, o diafragma deverá ser substituído.
