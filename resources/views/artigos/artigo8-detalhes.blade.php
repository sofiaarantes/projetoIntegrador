                                <ul>
                                    <li><b>Como fazer: </b>Sente-se confortavelmente ou deite-se. Coloque uma mão no peito e a outra no abdômen. Respire lentamente pelo nariz, sentindo o abdômen se expandir. Expire pela boca, permitindo que o abdômen se contraia. Repita várias vezes, concentrando-se na sensação de relaxamento que acompanha cada respiração.</li>
                                </ul>
                            </p>
