                            <p>
                                O yoga combina posturas corporais, respiração e meditação para promover o equilíbrio físico e mental:
                                <ul>
                                    <li><b>Como praticar: </b>Encontre uma aula de yoga local ou experimente seguir vídeos online. Comece com poses simples que focam na respiração e no alongamento. O yoga não apenas relaxa o corpo, mas também acalma a mente.</li>
                                </ul>
                            </p>
