                            <p>
                                Esta técnica envolve tensionar e relaxar conscientemente os músculos do corpo, promovendo um profundo relaxamento físico e mental:
                                <ul>
                                    <li><b>Como praticar: </b>Comece pelos pés e vá subindo até a cabeça. Contraia os músculos de uma área por cinco segundos e depois relaxe completamente por 30 segundos. Sinta a diferença entre a tensão e o relaxamento em cada parte do corpo enquanto progride.</li>
                                </ul>
                            </p>
                        <h2>Visualização Criativa</h2>
                            <p>
                                A visualização criativa utiliza a imaginação para criar cenários tranquilos e reconfortantes na mente, ajudando a reduzir a ansiedade e promover o relaxamento:
                                <ul>
                                    <li><b>Como fazer: </b>Feche os olhos e imagine um lugar calmo e sereno, como uma praia tranquila ou um jardim ensolarado. Explore os detalhes desse ambiente em sua mente: as cores, os sons, os cheiros. Permita-se mergulhar nessa experiência imaginária e aproveite a sensação de paz que ela traz.</li>
                                </ul>
                            </p>
