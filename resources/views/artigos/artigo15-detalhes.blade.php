                            <p>
                                Para tratar irritações ou desconfortos, escolha produtos formulados especificamente para a área genital:
                                <ul>
                                    <li><b>Cremes e Pomadas: </b>Cremes calmantes com ingredientes como camomila ou aloe vera podem aliviar irritações. Para infecções, use produtos recomendados por um médico.</li>
                                    <li><b>Protetores Diários: </b>Use protetores diários apenas quando necessário e escolha opções sem fragrância e com materiais respiráveis.</li>
