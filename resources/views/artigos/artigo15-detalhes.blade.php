                            <p>
                                Lubrificantes podem melhorar o conforto durante as relações sexuais, mas é importante escolher o tipo certo:
                                <ul>
                                    <li><b>À Base de Água: </b>São seguros para uso com preservativos e brinquedos sexuais. Eles são fáceis de limpar e geralmente não causam irritações.</li>
                                    <li><b>À Base de Silicone: </b>Duram mais tempo do que os à base de água e são ótimos para atividades aquáticas, mas podem danificar brinquedos sexuais de silicone.</li>
                                    <li><b>Naturais: </b>Opções feitas com ingredientes naturais, como aloe vera, são gentis para a pele e minimizam o risco de irritações.</li>
                                </ul>
                            </p>
