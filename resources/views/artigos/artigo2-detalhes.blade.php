                        <h2>Dicas para uma Alimentação Saudável</h2>
                            <p>
                                Aqui estão algumas dicas práticas para adotar uma alimentação saudável:
                                <ol>
                                    <li><b>Variedade é a Chave: </b>Inclua uma ampla variedade de alimentos em sua dieta para garantir a ingestão de todos os nutrientes essenciais. Frutas, legumes, grãos integrais, proteínas magras e laticínios devem estar presentes no seu cardápio.</li><br>
                                    <li><b>Escolha Alimentos Integrais: </b>Prefira alimentos integrais aos processados. Grãos integrais, como arroz integral e quinoa, mantêm mais nutrientes e fibras do que suas versões refinadas.</li><br>
                                    <li><b>Controle as Porções: </b>Comer porções adequadas ajuda a evitar o consumo excessivo de calorias. Use pratos menores e preste atenção aos sinais de saciedade do seu corpo.</li><br>
                                    <li><b>Hidrate-se: </b>A água é fundamental para o bom funcionamento do corpo. Beba pelo menos oito copos de água por dia e limite o consumo de bebidas açucaradas e alcoólicas.</li><br>
                                    <li><b>Planeje suas Refeições: </b>Planejar as refeições com antecedência pode ajudar a fazer escolhas mais saudáveis e evitar a tentação de optar por alimentos rápidos e pouco nutritivos.</li><br>
                                    <li><b>Cozinhe em Casa: </b>Preparar suas refeições permite controlar os ingredientes e as porções, além de ser uma ótima maneira de experimentar novos alimentos e receitas.</li><br>
                                    <li><b>Moderação é Importante: </b>Não se prive dos alimentos que você gosta. A chave está na moderação. Permita-se desfrutar de suas guloseimas favoritas ocasionalmente, sem exageros.</li><br>
                                </ol>
